// С помощью цикла while найдите сумму чисел от 1 до 20
// Результат присвойте переменной sum

// your code
let sum = 0; // sum — переменная, в которую будет накапливаться сумма чисел. Изначально она равна 0
let number = 1; // number — переменная, которая используется для перебора чисел от 1 до 20. Изначально она равна 1

while (number <= 20) { // Этот цикл выполняется, пока значение переменной number меньше или равно 20. Каждый раз, когда выполняется тело цикла, происходят следующие действия
    sum = sum + number; // К переменной sum прибавляется текущее значение переменной number
    number = number + 1; // Значение переменной number увеличивается на 1
}

console.log(sum); // После завершения цикла выводится итоговое значение переменной sum, которое содержит сумму чисел от 1 до 20


